package PyramidProgram;

public class Example1 {

	public static void main(String[] args) {
		for(int i=0;i<6;i++) {//define number of lines in pyramid
			for(int j=0;j<=i;j++) {//define pattern
				System.out.print(i+" ");
			}
			System.out.println();
		}
	

	for(char i='a';i<='f';i++) {//define number of lines in pyramid
		for(char j='a';j<=i;j++) {//define pattern
			System.out.print(j+" ");
		}
		System.out.println();
	}

	for(char i='a';i<='f';i++) {//define number of lines in pyramid
		for(char j='a';j<=i;j++) {//define pattern
			System.out.print(i+" ");
		}
		System.out.println();
	}
}

}
