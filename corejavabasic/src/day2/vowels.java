package day2;

public class vowels {

	public static void main(String[] args) {
		char x = 'i';
		if (x == 'a')
			System.out.println("print a");

		else if (x == 'e')
			System.out.println("print e");

		else if (x == 'i')
			System.out.println("print i");

		else if (x == 'o')
			System.out.println("print o");

		else if (x == 'u')
			System.out.println("u");
		else
			System.out.println("consonant");

	}
}
